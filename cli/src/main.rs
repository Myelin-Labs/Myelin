fn tool_check(name: &'static str) -> ToolCheck {
    let path = find_executable(name);
    ToolCheck { name, found: path.is_some(), path: path.map(|path| path.display().to_string()) }
}

fn find_executable(name: &str) -> Option<PathBuf> {
    let candidate = std::path::Path::new(name);
    if candidate.components().count() > 1 && is_executable(candidate) {
        return Some(candidate.to_path_buf());
    }
    let path_match = std::env::var_os("PATH")
        .and_then(|path_var| std::env::split_paths(&path_var).map(|dir| dir.join(name)).find(|candidate| is_executable(candidate)));
    path_match.or_else(|| fallback_tool_dirs().into_iter().map(|dir| dir.join(name)).find(|candidate| is_executable(candidate)))
}

fn fallback_tool_dirs() -> Vec<PathBuf> {
    ["/opt/homebrew/opt/llvm/bin", "/usr/local/opt/llvm/bin", "/opt/homebrew/opt/cmake/bin", "/usr/local/opt/cmake/bin"]
        .into_iter()
        .map(PathBuf::from)
        .collect()
}

fn is_executable(path: &std::path::Path) -> bool {
    path.is_file()
}

fn teeworlds_vm_probe(args: &TeeworldsVmProbeArgs) -> Result<TeeworldsVmProbeReport> {
    let replayer = fs::read(&args.replayer)?;
    let tape = fs::read(&args.tape)?;
    let map = fs::read(&args.map)?;
    let config = fs::read(&args.config)?;

    let script_code_hash = blake3_32(b"myelin:teeworlds-replayer-code:v1", &replayer);
    let lock_script = Script::new([0x11; 32], 0, vec![]);
    let type_script = Script::new(script_code_hash, 0, vec![]);
    let input_cell = CellOutput { lock: lock_script.clone(), type_: Some(type_script.clone()), capacity: 1_000 };
    let output_cell = CellOutput { lock: lock_script, type_: Some(type_script), capacity: 990 };
    let input_out_points = (0..4)
        .map(|index| {
            let mut tx_hash = [0x22; 32];
            tx_hash[0] = 0x22 + index as u8;
            OutPoint::new(tx_hash, 0)
        })
        .collect::<Vec<_>>();
    let tx = CellTx::new(
        input_out_points.iter().copied().map(|out_point| CellInput::new(out_point, 0)).collect(),
        vec![],
        vec![output_cell],
        vec![Vec::new()],
        vec![Vec::new(), tape.clone(), map.clone(), config.clone()],
    )
    .map_err(|error| CliError::InvalidFixture(error.to_owned()))?;

    let mut provider = SimpleDataProvider::new();
    provider.add_script(script_code_hash, replayer);
    for input_out_point in &input_out_points {
        provider.add_cell(
            input_out_point.tx_hash,
            input_out_point.index,
            ResolvedCell { cell_output: input_cell.clone(), data: Some(Vec::new()) },
        );
    }

    let verifier = TransactionScriptVerifier::new(Arc::new(tx), Arc::new(provider))
        .with_skip_lock_groups(true)
        .with_semantics(VmSemantics::CkbStrict)
        .with_version(ScriptVersion::latest())
        .with_max_cycles(args.max_cycles);

    let result = verifier.verify_with_cycles();
    let (success, cycles, error) = match result {
        Ok(cycles) => (true, Some(cycles), None),
        Err(error) => (false, None, Some(error.to_string())),
    };

    Ok(TeeworldsVmProbeReport {
        replayer: args.replayer.display().to_string(),
        tape_bytes: tape.len(),
        map_bytes: map.len(),
        config_bytes: config.len(),
        max_cycles: args.max_cycles,
        ckb_strict: true,
        success,
        cycles,
        error,
    })
}

fn teeworlds_court_bundle(
    path: PathBuf,
    chunk_bytes: usize,
    chunk_index: usize,
    consensus: &str,
) -> Result<TeeworldsCourtBundleReport> {
    if chunk_bytes == 0 {
        return Err(CliError::InvalidFixture("chunk-bytes must be non-zero".to_owned()));
    }
    let consensus_kind = match consensus {
        "static-closed-committee" | "static_closed_committee" => ConsensusKind::StaticClosedCommittee,
        "tendermint" => ConsensusKind::Tendermint,
        other => return Err(CliError::InvalidFixture(format!("unknown consensus engine for court-bundle: {other}"))),
    };

    let bytes = fs::read(&path)?;
    let fixture_hash = *blake3::hash(&bytes).as_bytes();
    let mock_tx_json_hash = hex::encode(fixture_hash);
    let value = serde_json::from_slice::<Value>(&bytes)?;
    let witnesses = find_witnesses(&value)
        .ok_or_else(|| CliError::InvalidFixture("could not find a witnesses array in mock transaction JSON".to_owned()))?;
    if witnesses.len() < 4 {
        return Err(CliError::InvalidFixture(format!("expected at least 4 witnesses, got {}", witnesses.len())));
    }

    let tape = &witnesses[1];
    let map = &witnesses[2];
    let config = &witnesses[3];
    let offset = chunk_index.checked_mul(chunk_bytes).ok_or_else(|| CliError::InvalidFixture("chunk offset overflow".to_owned()))?;
    if offset >= tape.len() {
        return Err(CliError::InvalidFixture(format!(
            "chunk index {chunk_index} is out of range for {} tape bytes and {chunk_bytes} byte chunks",
            tape.len()
        )));
    }

    let end = offset.saturating_add(chunk_bytes).min(tape.len());
    let chunk = &tape[offset..end];
    let commitment = chunk_commitment(chunk_index, offset, chunk);
    let map_hash = blake3_32(b"myelin:teeworlds-map:v1", map);
    let config_hash = blake3_32(b"myelin:teeworlds-config:v1", config);
    let tx = teeworlds_chunk_cell_tx(chunk_index, offset, chunk, commitment, map_hash, config_hash, fixture_hash)
        .map_err(|error| CliError::InvalidFixture(error.to_owned()))?;
    let ckb_projection = TeeworldsChunkProjectionReport::from(project_cell_tx_to_ckb(&tx));
    let molecule_transaction = serialize_transaction_molecule(&tx).map_err(|error| CliError::InvalidFixture(error.to_string()))?;
    let molecule_transaction_hash = blake3_32(b"myelin:ckb-molecule-transaction:v1", &molecule_transaction);

    let index_bytes = (chunk_index as u64).to_le_bytes();
    let session_id = blake3_chunks(b"myelin:teeworlds-session-id:v1", &[&fixture_hash]);
    let old_state_root = blake3_chunks(b"myelin:teeworlds-old-state-root:v1", &[&session_id, &index_bytes]);
    let new_state_root =
        blake3_chunks(b"myelin:teeworlds-new-state-root:v1", &[&old_state_root, &commitment, &map_hash, &config_hash]);
    let chunk_payload_hash = blake3_32(b"myelin:teeworlds-chunk-payload:v1", chunk);
    let scheduler_report_hash = blake3_chunks(b"myelin:teeworlds-scheduler-report:v1", &[&session_id, &index_bytes, &commitment]);

    // Build the canonical MyelinBlock from the runtime evidence. This is
    // the data-binding anchor: every field below is derived from the
    // session / chunk / map / config, and the block hash that the
    // finality certificate is bound to is `MyelinBlock::hash()` over
    // these exact fields.
    let canonical_block = MyelinBlock {
        version: 1,
        parent_hash: [0; 32],
        number: 1,
        timestamp_ms: 0,
        consensus_kind,
        state_root_before: old_state_root,
        state_root_after: new_state_root,
        ordered_cell_tx_commitments: vec![commitment],
        data_commitments: vec![commitment],
        scheduler_commitment: scheduler_report_hash,
    };
    let canonical_block_hash = canonical_block.hash();

    let (static_evidence, tendermint_evidence, evidence_notes) = match consensus_kind {
        ConsensusKind::StaticClosedCommittee => {
            // Build a static-closed-committee cert over the same canonical
            // block. The cert's `block_hash` MUST equal `canonical_block_hash`.
            let committee_config = static_fixture_committee_config();
            let committee = StaticClosedCommittee::new(committee_config.clone()).expect("static fixture committee");
            let signer_ids = vec!["validator-0".to_owned(), "validator-1".to_owned()];
            let cert = committee
                .certificate_for_fixture(canonical_block_hash, &["validator-0", "validator-1"])
                .expect("static fixture certificate");
            committee.finalise_block(canonical_block.clone(), cert.clone()).expect("static fixture finality");
            let signatures = cert
                .signatures
                .iter()
                .map(|signature| CommitteeSignatureEvidenceReport {
                    validator_id: signature.validator_id.clone(),
                    signature: hex::encode(signature.signature),
                    signature_hash: hex::encode(blake3_32(b"myelin:static-committee-signature-hash:v1", &signature.signature)),
                })
                .collect::<Vec<_>>();
            let evidence = StaticCommitteeEvidenceReport {
                consensus_kind: ConsensusKind::StaticClosedCommittee.as_str(),
                block_hash: hex::encode(canonical_block_hash),
                quorum_weight: committee_config.quorum_weight,
                signer_ids,
                signatures,
                finalised: true,
            };
            (evidence, None, vec!["This bundle carries static-closed-committee evidence over the canonical MyelinBlock.".to_owned()])
        }
        ConsensusKind::Tendermint => {
            // Build a Tendermint precommit cert over the same canonical
            // block. The cert's `block_hash` and `height` MUST equal
            // `canonical_block_hash` and `canonical_block.number`. The
            // Tendermint signature domain is independent of the static
            // committee's, so the two paths are not interchangeable.
            let tendermint_engine = tendermint_fixture_engine();
            let height = canonical_block.number;
            let round = 0u32;
            let signer_ids = vec!["validator-0".to_owned(), "validator-1".to_owned()];
            let cert = tendermint_engine
                .precommit_certificate_for_fixture(canonical_block_hash, height, round, &["validator-0", "validator-1"])
                .expect("tendermint fixture certificate");
            tendermint_engine
                .finalise_block_with_precommit(canonical_block.clone(), round, cert.clone())
                .expect("tendermint fixture finality");
            let signatures = cert
                .signatures
                .iter()
                .map(|signature| CommitteeSignatureEvidenceReport {
                    validator_id: signature.validator_id.clone(),
                    signature: hex::encode(signature.signature),
                    signature_hash: hex::encode(blake3_32(b"myelin:tendermint-precommit-signature-hash:v1", &signature.signature)),
                })
                .collect::<Vec<_>>();
            let tendermint_evidence = TendermintEvidenceReport {
                consensus_kind: ConsensusKind::Tendermint.as_str(),
                block_hash: hex::encode(canonical_block_hash),
                quorum_power: 2,
                height,
                round,
                signer_ids,
                signatures,
                certificate_step: "precommit",
                finalised: true,
            };
            // Static evidence is still emitted so the bundle surface stays
            // uniform, but it is informational only — the active finality
            // path is the Tendermint precommit.
            let committee_config = static_fixture_committee_config();
            let quorum_weight = committee_config.quorum_weight;
            let static_evidence = StaticCommitteeEvidenceReport {
                consensus_kind: ConsensusKind::StaticClosedCommittee.as_str(),
                block_hash: hex::encode(canonical_block_hash),
                quorum_weight,
                signer_ids: vec![],
                signatures: vec![],
                finalised: false,
            };
            let mut notes = vec!["This bundle carries Tendermint precommit evidence over the canonical MyelinBlock.".to_owned()];
            notes.push("The static-closed-committee evidence slot is informational; the challenge-payload-hash binds to the canonical block hash.".to_owned());
            (static_evidence, Some(tendermint_evidence), notes)
        }
    };
    let block_hash = canonical_block_hash;

    let challenge_payload_hash = blake3_chunks(
        b"myelin:single-chunk-challenge-payload:v1",
        &[&old_state_root, &chunk_payload_hash, &new_state_root, &scheduler_report_hash, &molecule_transaction_hash, &block_hash],
    );
    let court_verifiable = ckb_projection.ckb_projection_possible && ckb_projection.semantic_profile == "ckb-compatible";

    let mut notes = evidence_notes;
    notes.push("This is a single-chunk court input bundle for CKB-style adjudication design.".to_owned());
    notes.push(
        "It proves the disputed chunk has CKB-compatible transaction shape; it is not yet an on-chain CKB court script.".to_owned(),
    );

    let bundle_block = TeeworldsBundleBlock {
        version: canonical_block.version,
        parent_hash: hex::encode(canonical_block.parent_hash),
        number: canonical_block.number,
        timestamp_ms: canonical_block.timestamp_ms,
        consensus_kind: canonical_block.consensus_kind.as_str().to_string(),
        state_root_before: hex::encode(canonical_block.state_root_before),
        state_root_after: hex::encode(canonical_block.state_root_after),
        ordered_cell_tx_commitments: canonical_block.ordered_cell_tx_commitments.iter().map(hex::encode).collect(),
        data_commitments: canonical_block.data_commitments.iter().map(hex::encode).collect(),
        scheduler_commitment: hex::encode(canonical_block.scheduler_commitment),
        block_hash: hex::encode(canonical_block_hash),
    };

    Ok(TeeworldsCourtBundleReport {
        source: path.display().to_string(),
        mock_tx_json_hash,
        chunk_index,
        chunk_offset: offset,
        chunk_bytes: chunk.len(),
        chunk_payload_hex: hex::encode(chunk),
        session_id: hex::encode(session_id),
        old_state_root: hex::encode(old_state_root),
        new_state_root: hex::encode(new_state_root),
        chunk_payload_hash: hex::encode(chunk_payload_hash),
        map_hash: hex::encode(map_hash),
        config_hash: hex::encode(config_hash),
        chunk_commitment: hex::encode(commitment),
        scheduler_report_hash: hex::encode(scheduler_report_hash),
        block: bundle_block,
        molecule_transaction_bytes: molecule_transaction.len(),
        molecule_transaction_hex: hex::encode(&molecule_transaction),
        molecule_transaction_hash: hex::encode(molecule_transaction_hash),
        challenge_payload_hash: hex::encode(challenge_payload_hash),
        ckb_projection,
        static_committee_evidence: static_evidence,
        tendermint_evidence,
        court_verifiable,
        l1_court_implemented: false,
        notes,
    })
}

fn verify_teeworlds_court_bundle(path: PathBuf) -> Result<TeeworldsCourtBundleVerificationReport> {
    let bytes = fs::read(&path)?;
    let bundle = serde_json::from_slice::<TeeworldsCourtBundleInput>(&bytes)?;
    let mut checks = Vec::new();

    let chunk_payload = decode_hex_field(&bundle.chunk_payload_hex, "chunk_payload_hex")?;
    let expected_chunk_payload_hash = hex::encode(blake3_32(b"myelin:teeworlds-chunk-payload:v1", &chunk_payload));
    push_check(
        &mut checks,
        "chunk-payload-hash",
        expected_chunk_payload_hash == bundle.chunk_payload_hash,
        Some(expected_chunk_payload_hash),
        Some(bundle.chunk_payload_hash.clone()),
        "chunk payload hash matches embedded chunk payload",
    );

    let molecule_transaction = decode_hex_field(&bundle.molecule_transaction_hex, "molecule_transaction_hex")?;
    let expected_molecule_transaction_hash = hex::encode(blake3_32(b"myelin:ckb-molecule-transaction:v1", &molecule_transaction));
    push_check(
        &mut checks,
        "molecule-transaction-hash",
        expected_molecule_transaction_hash == bundle.molecule_transaction_hash,
        Some(expected_molecule_transaction_hash),
        Some(bundle.molecule_transaction_hash.clone()),
        "Molecule transaction hash matches embedded transaction bytes",
    );

    if let Some(expected_len) = bundle.ckb_projection.molecule_transaction_bytes {
        push_check(
            &mut checks,
            "molecule-transaction-length",
            expected_len == molecule_transaction.len(),
            Some(expected_len.to_string()),
            Some(molecule_transaction.len().to_string()),
            "projection byte length matches embedded transaction bytes",
        );
    } else {
        push_check(
            &mut checks,
            "molecule-transaction-length",
            false,
            None,
            Some(molecule_transaction.len().to_string()),
            "projection did not record a Molecule transaction length",
        );
    }

    let tx = deserialize_transaction_molecule(&molecule_transaction).map_err(|error| CliError::InvalidFixture(error.to_string()))?;
    let projection = project_cell_tx_to_ckb(&tx);
    let recomputed_projection = TeeworldsChunkProjectionReport::from(projection);
    push_check(
        &mut checks,
        "projection-possible",
        bundle.ckb_projection.ckb_projection_possible == recomputed_projection.ckb_projection_possible,
        Some(recomputed_projection.ckb_projection_possible.to_string()),
        Some(bundle.ckb_projection.ckb_projection_possible.to_string()),
        "projection possibility recomputes from embedded Molecule transaction",
    );
    push_check(
        &mut checks,
        "projection-profile",
        bundle.ckb_projection.semantic_profile == recomputed_projection.semantic_profile,
        Some(recomputed_projection.semantic_profile.clone()),
        Some(bundle.ckb_projection.semantic_profile.clone()),
        "semantic profile recomputes from embedded Molecule transaction",
    );
    push_check(
        &mut checks,
        "projection-source-txid",
        bundle.ckb_projection.source_txid == recomputed_projection.source_txid,
        Some(recomputed_projection.source_txid.clone()),
        Some(bundle.ckb_projection.source_txid.clone()),
        "source txid recomputes from embedded Molecule transaction",
    );
    push_check(
        &mut checks,
        "projection-raw-tx-hash",
        bundle.ckb_projection.ckb_raw_tx_hash == recomputed_projection.ckb_raw_tx_hash,
        recomputed_projection.ckb_raw_tx_hash.clone(),
        bundle.ckb_projection.ckb_raw_tx_hash.clone(),
        "CKB raw transaction hash recomputes from embedded Molecule transaction",
    );
    push_check(
        &mut checks,
        "projection-wtx-hash",
        bundle.ckb_projection.ckb_wtx_hash == recomputed_projection.ckb_wtx_hash,
        recomputed_projection.ckb_wtx_hash.clone(),
        bundle.ckb_projection.ckb_wtx_hash.clone(),
        "CKB witness transaction hash recomputes from embedded Molecule transaction",
    );

    let old_state_root = parse_hex_32(&bundle.old_state_root)
        .ok_or_else(|| CliError::InvalidFixture("old_state_root must be 32-byte hex".to_owned()))?;
    let chunk_payload_hash = parse_hex_32(&bundle.chunk_payload_hash)
        .ok_or_else(|| CliError::InvalidFixture("chunk_payload_hash must be 32-byte hex".to_owned()))?;
    let new_state_root = parse_hex_32(&bundle.new_state_root)
        .ok_or_else(|| CliError::InvalidFixture("new_state_root must be 32-byte hex".to_owned()))?;
    let scheduler_report_hash = parse_hex_32(&bundle.scheduler_report_hash)
        .ok_or_else(|| CliError::InvalidFixture("scheduler_report_hash must be 32-byte hex".to_owned()))?;
    let molecule_transaction_hash = parse_hex_32(&bundle.molecule_transaction_hash)
        .ok_or_else(|| CliError::InvalidFixture("molecule_transaction_hash must be 32-byte hex".to_owned()))?;

    // ─── Data-binding reconstruction ────────────────────────────────────
    // Reconstruct the canonical MyelinBlock from the bundle's data fields
    // and assert that its hash matches the supplied active block hash.
    // This is the structural guarantee that the finality certificate is
    // bound to THIS bundle's runtime evidence, not to a free-floating
    // claimed block hash.
    let reconstructed_block =
        bundle.block.to_myelin_block().map_err(|error| CliError::InvalidFixture(format!("bundle block: {error}")))?;
    let reconstructed_block_hash = reconstructed_block.hash();
    let supplied_block_hash = parse_hex_32(&bundle.block.block_hash)
        .ok_or_else(|| CliError::InvalidFixture("bundle.block.block_hash must be 32-byte hex".to_owned()))?;
    push_check(
        &mut checks,
        "block-hash-recomputes",
        reconstructed_block_hash == supplied_block_hash,
        Some(hex::encode(reconstructed_block_hash)),
        Some(hex::encode(supplied_block_hash)),
        "bundle.block fields rehash to bundle.block.block_hash",
    );

    // The block's runtime fields must match the bundle's runtime evidence.
    push_check(
        &mut checks,
        "block-state-root-before-matches",
        reconstructed_block.state_root_before == old_state_root,
        Some(hex::encode(reconstructed_block.state_root_before)),
        Some(hex::encode(old_state_root)),
        "bundle.block.state_root_before == bundle.old_state_root",
    );
    push_check(
        &mut checks,
        "block-state-root-after-matches",
        reconstructed_block.state_root_after == new_state_root,
        Some(hex::encode(reconstructed_block.state_root_after)),
        Some(hex::encode(new_state_root)),
        "bundle.block.state_root_after == bundle.new_state_root",
    );
    push_check(
        &mut checks,
        "block-scheduler-commitment-matches",
        reconstructed_block.scheduler_commitment == scheduler_report_hash,
        Some(hex::encode(reconstructed_block.scheduler_commitment)),
        Some(hex::encode(scheduler_report_hash)),
        "bundle.block.scheduler_commitment == bundle.scheduler_report_hash",
    );
    // The bundle's `chunk_commitment` field is what was committed to the
    // cell_data_hash chain. The block's `data_commitments[0]` MUST equal
    // that commitment.
    let bundle_chunk_commitment = parse_hex_32(&bundle.chunk_commitment)
        .ok_or_else(|| CliError::InvalidFixture("chunk_commitment must be 32-byte hex".to_owned()))?;
    push_check(
        &mut checks,
        "block-data-commitment-matches",
        reconstructed_block.data_commitments.first().copied() == Some(bundle_chunk_commitment),
        Some(hex::encode(reconstructed_block.data_commitments.first().copied().unwrap_or([0; 32]))),
        Some(hex::encode(bundle_chunk_commitment)),
        "bundle.block.data_commitments[0] == bundle.chunk_commitment",
    );
    // The active block hash is the reconstructed hash. The supplied
    // static-committee / Tendermint `block_hash` fields must equal it.
    let active_block_hash = reconstructed_block_hash;
    let supplied_evidence_block_hash = match &bundle.tendermint_evidence {
        Some(tm) => parse_hex_32(&tm.block_hash)
            .ok_or_else(|| CliError::InvalidFixture("tendermint block_hash must be 32-byte hex".to_owned()))?,
        None => parse_hex_32(&bundle.static_committee_evidence.block_hash)
            .ok_or_else(|| CliError::InvalidFixture("static committee block_hash must be 32-byte hex".to_owned()))?,
    };
    push_check(
        &mut checks,
        "evidence-block-hash-matches-canonical-block",
        supplied_evidence_block_hash == active_block_hash,
        Some(hex::encode(active_block_hash)),
        Some(hex::encode(supplied_evidence_block_hash)),
        "finality evidence block_hash matches the data-bound canonical block hash",
    );
    let expected_challenge_payload_hash = hex::encode(blake3_chunks(
        b"myelin:single-chunk-challenge-payload:v1",
        &[
            &old_state_root,
            &chunk_payload_hash,
            &new_state_root,
            &scheduler_report_hash,
            &molecule_transaction_hash,
            &active_block_hash,
        ],
    ));
    push_check(
        &mut checks,
        "challenge-payload-hash",
        expected_challenge_payload_hash == bundle.challenge_payload_hash,
        Some(expected_challenge_payload_hash),
        Some(bundle.challenge_payload_hash.clone()),
        "challenge payload hash recomputes from bundle fields",
    );

    let signature_hashes_ok = match &bundle.tendermint_evidence {
        Some(tm) => tm.signatures.iter().all(|signature| {
            decode_hex_field(&signature.signature, "signature")
                .map(|bytes| {
                    hex::encode(blake3_32(b"myelin:tendermint-precommit-signature-hash:v1", &bytes)) == signature.signature_hash
                })
                .unwrap_or(false)
        }),
        None => bundle.static_committee_evidence.signatures.iter().all(|signature| {
            decode_hex_field(&signature.signature, "signature")
                .map(|bytes| hex::encode(blake3_32(b"myelin:static-committee-signature-hash:v1", &bytes)) == signature.signature_hash)
                .unwrap_or(false)
        }),
    };
    let signature_count = match &bundle.tendermint_evidence {
        Some(tm) => tm.signatures.len(),
        None => bundle.static_committee_evidence.signatures.len(),
    };
    push_check(
        &mut checks,
        "committee-signature-hashes",
        signature_hashes_ok,
        Some("all signature hashes recompute".to_owned()),
        Some(format!("{} signatures", signature_count)),
        "each signature hash matches its embedded signature bytes",
    );

    let signer_ids = match &bundle.tendermint_evidence {
        Some(tm) => tm.signatures.iter().map(|signature| signature.validator_id.clone()).collect::<Vec<_>>(),
        None => bundle.static_committee_evidence.signatures.iter().map(|signature| signature.validator_id.clone()).collect::<Vec<_>>(),
    };
    let expected_signer_ids = match &bundle.tendermint_evidence {
        Some(tm) => &tm.signer_ids,
        None => &bundle.static_committee_evidence.signer_ids,
    };
    push_check(
        &mut checks,
        "committee-signer-ids",
        signer_ids == *expected_signer_ids,
        Some(format!("{:?}", signer_ids)),
        Some(format!("{:?}", expected_signer_ids)),
        "signer id list matches embedded certificate signatures",
    );

    if let Some(tm) = &bundle.tendermint_evidence {
        // Tendermint precommit certificate verification
        let tendermint_engine = tendermint_fixture_engine();
        let signatures = tm
            .signatures
            .iter()
            .map(|signature| {
                let bytes = parse_hex_64(&signature.signature)
                    .ok_or_else(|| CliError::InvalidFixture("tendermint signature must be 64-byte hex".to_owned()))?;
                Ok(CommitteeSignature { validator_id: signature.validator_id.clone(), signature: bytes })
            })
            .collect::<Result<Vec<_>>>()?;
        let certificate = myelin_consensus::TendermintPrecommitCertificate {
            block_hash: active_block_hash,
            height: tm.height,
            round: tm.round,
            signatures,
        };
        let certificate_ok =
            tendermint_engine.verify_precommit_certificate(active_block_hash, tm.height, tm.round, &certificate).is_ok();
        push_check(
            &mut checks,
            "tendermint-certificate",
            certificate_ok,
            Some(format!("quorum power {} height {} round {}", 2, tm.height, tm.round)),
            Some(format!("quorum power {}, finalised {}", tm.quorum_power, tm.finalised)),
            "Tendermint precommit certificate verifies against the phase-one fixture engine",
        );
        push_check(
            &mut checks,
            "tendermint-quorum-power",
            tm.quorum_power == 2,
            Some("2".to_owned()),
            Some(tm.quorum_power.to_string()),
            "bundle Tendermint quorum power matches the phase-one fixture engine",
        );
    } else {
        // Static-closed-committee certificate verification
        let block_hash = active_block_hash;
        let committee_config = static_fixture_committee_config();
        let committee = StaticClosedCommittee::new(committee_config.clone())?;
        let signatures = bundle
            .static_committee_evidence
            .signatures
            .iter()
            .map(|signature| {
                let bytes = parse_hex_64(&signature.signature)
                    .ok_or_else(|| CliError::InvalidFixture("committee signature must be 64-byte hex".to_owned()))?;
                Ok(CommitteeSignature { validator_id: signature.validator_id.clone(), signature: bytes })
            })
            .collect::<Result<Vec<_>>>()?;
        let certificate = CommitteeCertificate { block_hash, signatures };
        let certificate_ok = committee.verify_certificate(block_hash, &certificate).is_ok();
        push_check(
            &mut checks,
            "committee-certificate",
            certificate_ok,
            Some(format!("quorum weight {}", committee_config.quorum_weight)),
            Some(format!(
                "quorum weight {}, finalised {}",
                bundle.static_committee_evidence.quorum_weight, bundle.static_committee_evidence.finalised
            )),
            "static committee certificate verifies against the phase-one fixture committee",
        );
        push_check(
            &mut checks,
            "committee-quorum-weight",
            bundle.static_committee_evidence.quorum_weight == committee_config.quorum_weight,
            Some(committee_config.quorum_weight.to_string()),
            Some(bundle.static_committee_evidence.quorum_weight.to_string()),
            "bundle quorum weight matches the phase-one fixture committee",
        );
    }
    push_check(
        &mut checks,
        "court-verifiable-profile",
        bundle.ckb_projection.ckb_projection_possible && bundle.ckb_projection.semantic_profile == "ckb-compatible",
        Some("ckb-compatible + projection possible".to_owned()),
        Some(format!("{} + {}", bundle.ckb_projection.semantic_profile, bundle.ckb_projection.ckb_projection_possible)),
        "bundle is projectable into the CKB-compatible profile",
    );

    let valid = checks.iter().all(|check| check.ok);
    Ok(TeeworldsCourtBundleVerificationReport { bundle: path.display().to_string(), valid, checks })
}

