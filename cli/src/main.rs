type Result<T> = std::result::Result<T, CliError>;

fn main() {
    if let Err(error) = run() {
        eprintln!("error: {error}");
        std::process::exit(1);
    }
}

fn run() -> Result<()> {
    let cli = Cli::parse();
    match cli.command {
        Commands::Celltx(args) => match args.command {
            CelltxCommand::SimpleReport(args) => write_json(args.out, &simple_celltx_report()?),
        },
        Commands::Committee(args) => match args.command {
            CommitteeCommand::FinaliseDemo(args) => {
                let report = finalise_demo_from_config(args.config)?;
                write_json(args.out, &report)
            }
        },
        Commands::Teeworlds(args) => match args.command {
            TeeworldsCommand::Inspect(args) => {
                let report = inspect_teeworlds_mock_tx(args.mock_tx, args.chunk_bytes)?;
                write_json(args.out, &report)
            }
            TeeworldsCommand::Bench(args) => {
                let report = bench_teeworlds_mock_tx(args.mock_tx, args.chunk_bytes, args.runs)?;
                write_json(args.out, &report)
            }
            TeeworldsCommand::BuildFixture(args) => {
                let out = args.out.clone();
                let report = build_and_bench_teeworlds_fixture(args)?;
                write_json(out, &report)
            }
            TeeworldsCommand::VmProbe(args) => {
                let report = teeworlds_vm_probe(&args)?;
                write_json(args.out, &report)
            }
            TeeworldsCommand::CourtBundle(args) => {
                let report = teeworlds_court_bundle(args.mock_tx, args.chunk_bytes, args.chunk_index)?;
                write_json(args.out, &report)
            }
            TeeworldsCommand::VerifyCourtBundle(args) => {
                let report = verify_teeworlds_court_bundle(args.bundle)?;
                write_json(args.out, &report)
            }
            TeeworldsCommand::Doctor(args) => {
                let report = teeworlds_doctor(&args.teeworlds_root);
                write_json(args.out, &report)
            }
        },
    }
}

fn write_json<T: Serialize>(path: Option<PathBuf>, value: &T) -> Result<()> {
    let json = serde_json::to_string_pretty(value)?;
    if let Some(path) = path {
        fs::write(path, json)?;
    } else {
        println!("{json}");
    }
    Ok(())
}

fn simple_celltx_report() -> Result<serde_json::Value> {
    let tx = CellTx::new(
        vec![CellInput::new(OutPoint::new([1; 32], 0), 0)],
        vec![],
        vec![CellOutput { lock: Script::new([2; 32], 1, vec![0x42]), type_: None, capacity: 100 }],
        vec![b"myelin-simple-output".to_vec()],
        vec![b"myelin-simple-witness".to_vec()],
    )
    .map_err(|error| CliError::InvalidFixture(error.to_owned()))?;
    let report = build_cell_tx_execution_report(&tx, [0; 32]);
    Ok(serde_json::to_value(report)?)
}

