fn find_witnesses(value: &Value) -> Option<Vec<Vec<u8>>> {
    match value {
        Value::Object(object) => {
            if let Some(witnesses) = object.get("witnesses").and_then(decode_witness_array) {
                return Some(witnesses);
            }
            for child in object.values() {
                if let Some(witnesses) = find_witnesses(child) {
                    return Some(witnesses);
                }
            }
            None
        }
        Value::Array(values) => values.iter().find_map(find_witnesses),
        _ => None,
    }
}

fn decode_witness_array(value: &Value) -> Option<Vec<Vec<u8>>> {
    let values = value.as_array()?;
    values.iter().map(decode_json_bytes).collect::<Option<Vec<_>>>()
}

fn decode_json_bytes(value: &Value) -> Option<Vec<u8>> {
    match value {
        Value::String(value) => decode_hex_bytes(value),
        Value::Object(object) => object
            .get("content")
            .or_else(|| object.get("data"))
            .or_else(|| object.get("jsonbytes"))
            .and_then(Value::as_str)
            .and_then(decode_hex_bytes),
        _ => None,
    }
}

fn decode_hex_bytes(value: &str) -> Option<Vec<u8>> {
    let stripped = value.strip_prefix("0x").unwrap_or(value);
    if stripped.is_empty() {
        return Some(Vec::new());
    }
    hex::decode(stripped).ok()
}

fn chunk_commitment(index: usize, offset: usize, chunk: &[u8]) -> [u8; 32] {
    let mut hasher = blake3::Hasher::new();
    hasher.update(b"myelin:teeworlds-fixture-chunk:v1");
    hasher.update(&(index as u64).to_le_bytes());
    hasher.update(&(offset as u64).to_le_bytes());
    hasher.update(&(chunk.len() as u64).to_le_bytes());
    hasher.update(chunk);
    *hasher.finalize().as_bytes()
}

fn teeworlds_chunk_projection(
    index: usize,
    offset: usize,
    chunk: &[u8],
    commitment: [u8; 32],
    map_hash: [u8; 32],
    config_hash: [u8; 32],
    fixture_hash: [u8; 32],
) -> TeeworldsChunkProjectionReport {
    let tx = teeworlds_chunk_cell_tx(index, offset, chunk, commitment, map_hash, config_hash, fixture_hash)
        .expect("Teeworlds chunk CellTx shape is internally consistent");
    TeeworldsChunkProjectionReport::from(project_cell_tx_to_ckb(&tx))
}

fn teeworlds_chunk_cell_tx(
    index: usize,
    offset: usize,
    chunk: &[u8],
    commitment: [u8; 32],
    map_hash: [u8; 32],
    config_hash: [u8; 32],
    fixture_hash: [u8; 32],
) -> std::result::Result<CellTx, &'static str> {
    let index_bytes = (index as u64).to_le_bytes();
    let input_tx_hash = blake3_chunks(b"myelin:teeworlds-session-input:v1", &[&fixture_hash, &index_bytes]);
    let type_code_hash = blake3_32(b"myelin:teeworlds-session-type:v1", &fixture_hash);
    let lock_script = Script::new([0x51; 32], 1, fixture_hash.to_vec());
    let mut type_args = Vec::with_capacity(40);
    type_args.extend_from_slice(&fixture_hash);
    type_args.extend_from_slice(&index_bytes);
    let type_script = Script::new(type_code_hash, 1, type_args);

    let input = CellInput::new(OutPoint::new(input_tx_hash, 0), 0);
    let output = CellOutput { lock: lock_script, type_: Some(type_script), capacity: 1_000 };
    let output_data = teeworlds_chunk_output_data(index, offset, chunk.len(), commitment, map_hash, config_hash);
    CellTx::new(vec![input], vec![], vec![output], vec![output_data], vec![chunk.to_vec()])
}

fn teeworlds_chunk_output_data(
    index: usize,
    offset: usize,
    byte_len: usize,
    commitment: [u8; 32],
    map_hash: [u8; 32],
    config_hash: [u8; 32],
) -> Vec<u8> {
    let mut out = Vec::with_capacity(24 + 32 * 3);
    out.extend_from_slice(&(index as u64).to_le_bytes());
    out.extend_from_slice(&(offset as u64).to_le_bytes());
    out.extend_from_slice(&(byte_len as u64).to_le_bytes());
    out.extend_from_slice(&commitment);
    out.extend_from_slice(&map_hash);
    out.extend_from_slice(&config_hash);
    out
}

fn blake3_chunks(domain: &[u8], chunks: &[&[u8]]) -> [u8; 32] {
    let mut hasher = blake3::Hasher::new();
    hasher.update(domain);
    for chunk in chunks {
        hasher.update(chunk);
    }
    *hasher.finalize().as_bytes()
}

fn blake3_32(domain: &[u8], data: &[u8]) -> [u8; 32] {
    let mut hasher = blake3::Hasher::new();
    hasher.update(domain);
    hasher.update(data);
    *hasher.finalize().as_bytes()
}

fn parse_hex_32(value: &str) -> Option<[u8; 32]> {
    let bytes = hex::decode(value).ok()?;
    bytes.try_into().ok()
}

fn parse_hex_64(value: &str) -> Option<[u8; 64]> {
    let bytes = hex::decode(value).ok()?;
    bytes.try_into().ok()
}

impl From<CkbProjectionReport> for TeeworldsChunkProjectionReport {
    fn from(report: CkbProjectionReport) -> Self {
        Self {
            semantic_profile: semantic_profile_label(report.semantic_profile).to_string(),
            ckb_projection_possible: report.ckb_projection_possible,
            blockers: serde_json::to_value(report.blockers).expect("projection blockers serialize"),
            warnings: serde_json::to_value(report.warnings).expect("projection warnings serialize"),
            source_txid: hex::encode(report.source_txid),
            ckb_raw_tx_hash: report.ckb_raw_tx_hash.map(hex::encode),
            ckb_wtx_hash: report.ckb_wtx_hash.map(hex::encode),
            molecule_transaction_bytes: report.molecule_transaction_bytes,
            input_count: report.input_count,
            output_count: report.output_count,
            witness_count: report.witness_count,
        }
    }
}

fn semantic_profile_label(profile: SemanticProfile) -> &'static str {
    match profile {
        SemanticProfile::MyelinNative => "myelin-native",
        SemanticProfile::CkbCompatible => "ckb-compatible",
        SemanticProfile::CkbInspiredOnly => "ckb-inspired-only",
    }
}

