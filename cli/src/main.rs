fn inspect_teeworlds_mock_tx(path: PathBuf, chunk_bytes: usize, consensus: &str) -> Result<TeeworldsFixtureReport> {
    if chunk_bytes == 0 {
        return Err(CliError::InvalidFixture("chunk-bytes must be non-zero".to_owned()));
    }
    let consensus_kind = parse_teeworlds_consensus(consensus)?;

    let bytes = fs::read(&path)?;
    let fixture_hash = *blake3::hash(&bytes).as_bytes();
    let mock_tx_json_hash = hex::encode(fixture_hash);
    let value = serde_json::from_slice::<Value>(&bytes)?;
    let witnesses = find_witnesses(&value)
        .ok_or_else(|| CliError::InvalidFixture("could not find a witnesses array in mock transaction JSON".to_owned()))?;
    if witnesses.len() < 4 {
        return Err(CliError::InvalidFixture(format!("expected at least 4 witnesses, got {}", witnesses.len())));
    }

    let witness_bytes = witnesses.iter().map(Vec::len).collect::<Vec<_>>();
    let tape = &witnesses[1];
    let map = &witnesses[2];
    let config = &witnesses[3];
    let map_hash = blake3_32(b"myelin:teeworlds-map:v1", map);
    let config_hash = blake3_32(b"myelin:teeworlds-config:v1", config);
    let chunks = tape
        .chunks(chunk_bytes)
        .enumerate()
        .map(|(index, chunk)| {
            let offset = index * chunk_bytes;
            let commitment = chunk_commitment(index, offset, chunk);
            let ckb_projection = teeworlds_chunk_projection(index, offset, chunk, commitment, map_hash, config_hash, fixture_hash);
            TeeworldsChunkReport { index, offset, byte_len: chunk.len(), commitment: hex::encode(commitment), ckb_projection }
        })
        .collect::<Vec<_>>();
    let ckb_projection_possible = chunks.iter().all(|chunk| chunk.ckb_projection.ckb_projection_possible);
    let finality = finalise_teeworlds_fixture_block(&chunks, consensus_kind);

    Ok(TeeworldsFixtureReport {
        source: path.display().to_string(),
        mock_tx_json_hash,
        witness_count: witnesses.len(),
        witness_bytes,
        tape_bytes: tape.len(),
        map_bytes: map.len(),
        config_bytes: config.len(),
        chunk_bytes,
        ckb_projection_possible,
        chunks,
        finality,
    })
}

fn parse_teeworlds_consensus(consensus: &str) -> Result<ConsensusKind> {
    match consensus {
        "static-closed-committee" | "static_closed_committee" => Ok(ConsensusKind::StaticClosedCommittee),
        "tendermint" => Ok(ConsensusKind::Tendermint),
        other => Err(CliError::InvalidFixture(format!("unknown consensus engine: {other}"))),
    }
}

