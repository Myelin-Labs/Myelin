fn find_witnesses(value: &Value) -> Option<Vec<Vec<u8>>> {
    match value {
        Value::Object(object) => {
            if let Some(witnesses) = object.get("witnesses").and_then(decode_witness_array) {
                return Some(witnesses);
            }
            for child in object.values() {
                if let Some(witnesses) = find_witnesses(child) {
                    return Some(witnesses);
                }
            }
            None
        }
        Value::Array(values) => values.iter().find_map(find_witnesses),
        _ => None,
    }
}

fn decode_witness_array(value: &Value) -> Option<Vec<Vec<u8>>> {
    let values = value.as_array()?;
    values.iter().map(decode_json_bytes).collect::<Option<Vec<_>>>()
}

fn decode_json_bytes(value: &Value) -> Option<Vec<u8>> {
    match value {
        Value::String(value) => decode_hex_bytes(value),
        Value::Object(object) => object
            .get("content")
            .or_else(|| object.get("data"))
            .or_else(|| object.get("jsonbytes"))
            .and_then(Value::as_str)
            .and_then(decode_hex_bytes),
        _ => None,
    }
}

fn decode_hex_bytes(value: &str) -> Option<Vec<u8>> {
    let stripped = value.strip_prefix("0x").unwrap_or(value);
    if stripped.is_empty() {
        return Some(Vec::new());
    }
    hex::decode(stripped).ok()
}

fn chunk_commitment(index: usize, offset: usize, chunk: &[u8]) -> [u8; 32] {
    let mut hasher = blake3::Hasher::new();
    hasher.update(b"myelin:teeworlds-fixture-chunk:v1");
    hasher.update(&(index as u64).to_le_bytes());
    hasher.update(&(offset as u64).to_le_bytes());
    hasher.update(&(chunk.len() as u64).to_le_bytes());
    hasher.update(chunk);
    *hasher.finalize().as_bytes()
}

